# In PowerShell:
# 1. Open PowerShell and navigate to the directory where you want to create your repository by using
# cd or Set-Location. For example cd 'path_to_your_directory' OR Set-Location 'path_to_your_directory'.

# 2. Initialize a new Git repository.
#   git init

# 3. Check the status of the repository.
#   git status

# 4. Create a new file named README.md in the repository and add some content.
#   New-Item README.md -ItemType file
#   Set-Content README.md "# My New Repository"

# 5. Add the file to the staging area.
#   git add README.md

# 6. Add the python file to the staging area.
#   git add xxx.py (git add . means add all files)

# 7. Check the status of the repository again to see whether it is correct.
#   git status

# 8. Commit the changes with a message.
#   git commit -m "Initial commit"

# 9. Go to the remote repository (e.g., GitHub) and create a new repository. Copy the remote repository URL.

# 10. Link the local repository to the remote repository by setting the remote origin.
#   git remote add origin 'remote_repository_url'

# 11. Push the local commits to the remote repository.
#   git push -u origin master (For some user, it might be main)

# In Terminal (bash):
# 1. Open Terminal and navigate to the directory where you want to create your repository.
#   cd /path/to/your/directory

# 2. Initialize a new Git repository.
#   git init

# 3. Check the status of the repository.
#   git status

# 4. Create a new file in your repository and add some content.
#   touch README.md
#   echo "# My New Repository" > README.md

# 5. Add the file to the staging area.
#   git add README.md

# 6. Add the python file to the staging area.
#   git add xxx.py (git add . means add all files)

# 7. Check the status of the repository again to see whether it is correct.
#   git status

# 8. Commit the changes with a message.
#   git commit -m "Initial commit"

# 9. Go to the remote repository (e.g., GitHub) and create a new repository. Copy the remote repository URL.

# 10. Link the local repository to the remote repository by setting the remote origin.
#   git remote add origin 'remote_repository_url'

# 11. Push the local commits to the remote repository.
#   git push -u origin master (For some user, it might be main)
